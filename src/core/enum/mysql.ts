export enum MYSQL_FIELDS_TYPES {
  DECIMAL = 0x00, // aka DECIMAL (http://dev.mysql.com/doc/refman/5.0/en/precision-math-decimal-changes.html)
  TINY = 0x01, // aka TINYINT, 1 byte
  SHORT = 0x02, // aka SMALLINT, 2 bytes
  LONG = 0x03, // aka INT, 4 bytes
  FLOAT = 0x04, // aka FLOAT, 4-8 bytes
  DOUBLE = 0x05, // aka DOUBLE, 8 bytes
  NULL = 0x06, // NULL (used for prepared statements, I think)
  TIMESTAMP = 0x07, // aka TIMESTAMP
  LONGLONG = 0x08, // aka BIGINT, 8 bytes
  INT24 = 0x09, // aka MEDIUMINT, 3 bytes
  DATE = 0x0a, // aka DATE
  TIME = 0x0b, // aka TIME
  DATETIME = 0x0c, // aka DATETIME
  YEAR = 0x0d, // aka YEAR, 1 byte (don't ask)
  NEWDATE = 0x0e, // aka ?
  VARCHAR = 0x0f, // aka VARCHAR (?)
  BIT = 0x10, // aka BIT, 1-8 byte
  JSON = 0xf5,
  NEWDECIMAL = 0xf6, // aka DECIMAL
  ENUM = 0xf7, // aka ENUM
  SET = 0xf8, // aka SET
  TINY_BLOB = 0xf9, // aka TINYBLOB, TINYTEXT
  MEDIUM_BLOB = 0xfa, // aka MEDIUMBLOB, MEDIUMTEXT
  LONG_BLOB = 0xfb, // aka LONGBLOG, LONGTEXT
  BLOB = 0xfc, // aka BLOB, TEXT
  VAR_STRING = 0xfd, // aka VARCHAR, VARBINARY
  STRING = 0xfe, // aka CHAR, BINARY
  GEOMETRY = 0xff, // aka GEOMETRY
}

// column definition flags
export enum MYSQL_FIELDS_FLAGS {
  /** Field can't be NULL */
  NOT_NULL_FLAG = 1 << 0,
  /** Field is part of a primary key */
  PRI_KEY_FLAG = 1 << 1,
  /** Field is part of a unique key */
  UNIQUE_KEY_FLAG = 1 << 2,
  /** Field is part of a key */
  MULTIPLE_KEY_FLAG = 1 << 3,
  /** Field is a blob */
  BLOB_FLAG = 1 << 4,
  /** Field is unsigned */
  UNSIGNED_FLAG = 1 << 5,
  /** Field is zerofill */
  ZEROFILL_FLAG = 1 << 6,
  /** Field is binary */
  BINARY_FLAG = 1 << 7,
  // The following are only sent to new clients
  /** field is an enum */
  ENUM_FLAG = 1 << 8,
  /** field is a autoincrement field */
  AUTO_INCREMENT_FLAG = 1 << 9,
  /** Field is a timestamp */
  TIMESTAMP_FLAG = 1 << 10,
  /** field is a set */
  SET_FLAG = 1 << 11,
  /** Field doesn't have default value */
  NO_DEFAULT_VALUE_FLAG = 1 << 12,
  /** Field is set to NOW on UPDATE */
  ON_UPDATE_NOW_FLAG = 1 << 13,
  /** Field is num (for clients) */
  NUM_FLAG = 1 << 15,
  /** < Intern; Part of some key */
  PART_KEY_FLAG = 1 << 14,
  /** < Intern: Group field */
  GROUP_FLAG = 1 << 15,
  /** < Intern: Used by sql_yacc */
  UNIQUE_FLAG = 1 << 16,
  /** < Intern: Used by sql_yacc */
  BINCMP_FLAG = 1 << 17,
  /** Used to get fields in item tree */
  GET_FIXED_FIELDS_FLAG = 1 << 18,
  /** Field part of partition func */
  FIELD_IN_PART_FUNC_FLAG = 1 << 19,
  FIELD_IN_ADD_INDEX = 1 << 20,
  /** < Intern: Field is being renamed */
  FIELD_IS_RENAMED = 1 << 21,
  /** Field storage media, bit 22-23 */
  FIELD_FLAGS_STORAGE_MEDIA = 22,
  FIELD_FLAGS_STORAGE_MEDIA_MASK = 3 << FIELD_FLAGS_STORAGE_MEDIA,
  /** < Field column format, bit 24-25 */
  FIELD_FLAGS_COLUMN_FORMAT = 24,
  FIELD_FLAGS_COLUMN_FORMAT_MASK = 3 << FIELD_FLAGS_COLUMN_FORMAT,
  /** < Intern: Field is being dropped */
  FIELD_IS_DROPPED = 1 << 26,
  /** < Field is explicitly specified as NULL by the user */
  EXPLICIT_NULL_FLAG = 1 << 27,
  /** 1 << 28 is unused. */
  /** Field will not be loaded in secondary engine. */
  NOT_SECONDARY_FLAG = 1 << 29,
  /** Field is explicitly marked as invisible by the user. */
  FIELD_IS_INVISIBLE = 1 << 30
}

